"""
Tipo None

O tipo de dado None em Python representa o tpo sem tipo, ou poderia ser conhecido também como tipo vazio,
porem, falar que é um tipo sem tipo é mais apropriado.

Obs: o tipo None é sempre especificado com a primeira letra maiúscula.

Quando utilizar?

- Podemos utilizar None quando queremos criar ums variável e inicializá-la com um tipo sem tipo, antes
de recebermos um valor final.

OBS: O tipo none em Python é SEMPRE considerado como False
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1,34, 5.67

print(numeros)
print(type(numeros))